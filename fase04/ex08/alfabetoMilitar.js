function alfabetoMilitar (letra) {
    letra = letra.toLowerCase ()
    if (letra === "a")
    return "alpha"
    else if (letra === "e")
    return "echo"
    else if (letra === "i")
    return "india"
    else if (letra === "o")
    return "oscar"
    else if (letra === "u")
    return "uniform"
    else 
    return false
}